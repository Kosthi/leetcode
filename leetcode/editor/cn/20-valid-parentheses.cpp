class Solution {
public:
    bool isValid(string s) {
        std::stack<char> stack;
        for (auto &c : s) {
            if (c == '(') {
                stack.emplace(')');
            } else if (c == '{') {
                stack.emplace('}');
            } else if (c == '[') {
                stack.emplace(']');
            } else if (stack.empty() || c != stack.top()) {
                return false;
            } else {
                stack.pop();
            }
        }
        return stack.empty();
    }
};
